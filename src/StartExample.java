public class StartExample {

    /*
        psvm + tab или psvm + ctrl + space
        "Входная точка" или стартовый метод,
        с которого запускается java-программа
     */
    public static void main(String[] args) {

        /*
            sout + tab
            Метод выводящий информацию в консоль
         */
        System.out.println("Hello world");

        /*
            Shift + F10
            Запуск программы для исполнения

            Также можно использовать значок "зеленой стрелки"
            слева относительно записи
            "public static void main(String[] args)"
         */
    }
}